import { spawnSync } from "node:child_process";
import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

const repositoryRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const corePath = path.join(repositoryRoot, "target", "debug", process.platform === "win32" ? "siaocut-core.exe" : "siaocut-core");
const outputPath = path.join(repositoryRoot, "apps", "desktop", "src", "generated", "core-contract.ts");
const result = spawnSync(corePath, ["--json", "contract"], {
  cwd: repositoryRoot,
  encoding: "utf8",
  windowsHide: true,
  env: { ...process.env, SIAOCUT_DIRECT: "1" },
});

if (result.status !== 0) {
  throw new Error(`Unable to export the Rust Core contract. Run cargo build first.\n${result.stderr}`);
}

const contract = JSON.parse(result.stdout);
const declarations = [
  ["backgroundJobStatuses", "BackgroundJobStatus", contract.statusSets.backgroundJob],
  ["transcriptionJobStatuses", "TranscriptionJobStatus", contract.statusSets.transcriptionJob],
  ["taskStatuses", "TaskStatus", contract.statusSets.task],
  ["workflowStatuses", "WorkflowStatus", contract.statusSets.workflow],
  ["autoWorkflowStatuses", "AutoWorkflowStatus", contract.statusSets.autoWorkflow],
  ["autoWorkflowStages", "AutoWorkflowStage", contract.statusSets.autoWorkflowStage],
  ["coreErrorCodes", "KnownCoreErrorCode", contract.errorCodes],
];
const source = [
  "// Generated by tools/generate-core-contract.mjs from the Rust Core contract command.",
  "// Do not edit this file directly.",
  "",
  ...declarations.flatMap(([constantName, typeName, values]) => [
    `export const ${constantName} = ${JSON.stringify(values, null, 2)} as const;`,
    `export type ${typeName} = typeof ${constantName}[number];`,
    "",
  ]),
  "export type CoreErrorCode = KnownCoreErrorCode | (string & {});",
  "",
].join("\n");

if (process.argv.includes("--check")) {
  let current = "";
  try {
    current = readFileSync(outputPath, "utf8");
  } catch {
    // Report the same actionable drift message for a missing generated file.
  }
  if (current !== source) {
    throw new Error("Generated Core contract is stale. Run: node tools/generate-core-contract.mjs");
  }
} else {
  writeFileSync(outputPath, source, "utf8");
}
